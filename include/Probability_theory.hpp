/*
	********************************************************************************************************************
	Представленный метод вычисления 'среднего значения' и 'стандартного отклонения' 
	адекватен для многих приложений, 
	однако имеет два ограничения. 
	
	Первое, если 'среднее значение'

	µ	=	(x𝚘 + x𝟷 + ... + x𝚗-𝟷)   / N;
	
	намного "больше", чем 'стандартное отклонение', 
	
	σ	=	√ {[(x𝚘  -  µ)² + (x𝟷 -  µ)² + ... + (x𝚗-𝟷 -  µ)² )]   /  (N - 1)};	
	
	то в формуле расчёта 'дисперсии' 
	
	σ²	=	[(x𝚘  -  µ)² + (x𝟷 -  µ)² + ... + (x𝚗-𝟷 -  µ)² )]   /  (N - 1);		
	
	будет возводиться в квадрат 
	разница двух очень близких по величине чисел. 
	
	Это может привести к 'чрезмерной ошибке округления' в вычислениях, 
	более детально эта тема будет обсуждена в Главе 4. 
	
	Второе, когда 'новые отсчеты' получены и добавлены к сигналу, 
	очень часто желательно 
	'пересчитать' "среднее значение" и "стандартное отклонение". 
	
	Мы будем называть такой тип вычислений "бегущей статистикой" (а может быть, и "скользящей статистикой"). 
	
	Повидимому что-то похожее ранее я использовал под термином "усреднение с нарастающим итогом".
	
	При использовании в 'бегущей статистике' метода уравнений, используемого выше,
	требуется, чтобы все отсчеты были включены в каждое новое вычисление. 
	
	Это очень неэффективное использование вычислительной мощности и памяти. 
	
	Можно так определить дисперсию:
	
	σ²	=	[	(x𝚘 ² + x𝟷² + ... + x𝚗-𝟷 ² ) 
			- 	(x𝚘 + x𝟷 + ... + x𝚗-𝟷 )² / N
			]   
		/  
			(N - 1);		
	
	Проверим это на примерах функций, рассчитываюзщи скользящие средние,
	скользящие дисперсии и, соответственно, скользящие страндартные отклонения:
*/
//	-----------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая скользящее среднее значение отсчетов сигнала (среднее с нараствющим итогом)
static double 	moving_mean	(	double 	prev_moving_mean	//	Предыдущее скользящее среднее отсчетов 
							,	double 	current_sample	//	Добавляемый в скользящее среднее текущий отсчет
							,	int		N				//	Номер текущего отсчета
							)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	return	(current_sample + prev_moving_mean * N) / (N + 1);
}
//	-----------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая сумму отсчетов сигнала
static double 	moving_sum	(	double 	prev_moving_sum	//	Предыдущая скользящая сумма отсчетов 
							,	double 	current_sample	//	Добавляемый в скользящую сумму текущий отсчет
							)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	return	current_sample + prev_moving_sum;
}
//	-------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая сумму квадратов отсчетов сигнала
static double 	moving_sum_of_sqr	(	double 	prev_moving_sum_of_sqr	//	Предыдущее скользящая сумма квадратов отсчетов 
								,	double 	current_sample	//	Добавляемый в скользящую сумму квадратов текущий отсчет
								)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	return	std::pow(current_sample, 2) + prev_moving_sum_of_sqr;
}
//	-----------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая скользящую дисперсию отсчетов сигнала
static double 	moving_variance	(	double 	moving_sum			//	Скользящая сумма отсчетов 
								,	double 	moving_sum_of_sqr		//	Скользящая сумма квадратов отсчетов 
								,	int		N					//	Номер текущего отсчета
								)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	if (1 == N)
		return 0.0;
	else
		return		(moving_sum_of_sqr - std::pow(moving_sum, 2) / N) 
				/ 	(N - 1);
}
