/*
	*****************************************************************************************************************
	Среднее значение и стандартное отклонение 
	
	'Среднее значение' обозначается как 'µ' (греческая прописная буква 'мю'), 
	это жаргон статистики для обозначения 'средней величины сигнала'. 
	
	Оно находится так же, как Вы бы и ожидали: 
	сложить все отсчеты вместе и разделить на 'N'. 
	
	'Отсчёт'	(sample) -	'численное значение' сигнала 'в определённый момент времени'.
	
	µ	=	(x𝚘 + x𝟷 + ... + x𝚗-𝟷)   / N;
	
	Выражения 	| x𝚒	-  µ |  	показывает 'отклонение' (величину отличия) 
							i-го отсчета от 'среднего значения'.
	
	'Среднее отклонение' сигнала 
	находится суммированием 'отклонений' всех индивидуальных отсчетов, 
	а затем делением результата на количество отсчетов N. 
	
	Заметьте, что до суммирования мы берем 'абсолютную величину' 'каждого отклонения', 
	в противном случае положительные и отрицательные составляющие будут усредняться к нулю. 
	
	'Среднее отклонение' дает число, 
	показывающее 'типичное расстояние отсчетов' от 'среднего значения'.

	'Стандартное отклонение' подобно 'среднему отклонению' за исключением того, 
	что 'вместо усреднения амплитуды', 
	проводится 'усреднение мощности'. 
	
	Это достигается возведением каждого отклонения в квадрат перед тем, 
	как определить 'среднее' (вспомните, мощность пропорциональна напряжению). 
	
	Для компенсации первоначального возведения в квадрат, 
	в конце извлекается квадратный корень. 
	
	В форме уравнения 'стандартное отклонение' вычисляется как:
	
	σ²	=	[(x𝚘  -  µ)² + (x𝟷 -  µ)² + ... + (x𝚗-𝟷 -  µ)² )]   /  (N - 1);			!!!!!!!
	
	σ	=	√ {[(x𝚘  -  µ)² + (x𝟷 -  µ)² + ... + (x𝚗-𝟷 -  µ)² )]   /  (N - 1)};		!!!!!!!
	
	!!!!!!!!
	Обратите внимание, что 'среднее' 
	получается делением на (N-1) вместо N. 
	
	Это тонкая особенность уравнения, которая будет обсуждена в следующем разделе. 
	
	Термин σ2 часто встречается в статистике, 
	и ему присвоено название "выборочная дисперсия". 
	
	'Стандартное отклонение' - это мера того, 
	насколько далеко сигнал флюктуирует от среднего значения. 
	
	'Выборочная дисперсия' представляет 'мощность' этих флюктуаций. 
	
	Другой, часто используемый в электронике термин, 
	с которым Вы должны легко обращаться, 
	это "среднеквадратичная величина". 
	
	По определению 'стандартное отклонение' 
	измеряет 'только переменную составляющую сигнала', 
	в то время как 'среднеквадратичная величина' 
	'измеряет совместно' 
	как 'переменную', 
	так и 'постоянную'
	'составляющую сигнала'. 
	
	Если 'постоянная составляющая' в сигнале 'отсутствует', 
	то его 'среднеквадратичная величина' 
	идентична его 'стандартному отклонению'.
*/
//	----------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая среднее значение отсчетов сигнала
#include <cmath>
static double 	mean	(	double 	signals_array[]	//	Массив значений отсчетов сигнала
					,	int		N				//	Количество отсчетов в массиве
					)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;		
	double	signals_sum{0.0};
	for (int i = 0;	i < N; ++i)
		signals_sum += signals_array[i];
	return signals_sum / N;
}	
//	----------------------------------------------------------------------------------------------------------------------------------------------------------------
//	Функция, возвращающая дисперсию отсчетов сигнала
static double 	variance	(	double 	signals_array[]	//	Массив значений отсчетов сигнала
						,	int		N				//	Количество отсчетов  в массиве
						)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	
	double 	mean_value	=	mean	(	signals_array	//	Массив значений отсчетов сигнала
								,	N				//	Количество отсчетов в массиве
								);
								
	//	Cумма квадратов отклонений случайных величин от среднего значения	отсчетов сигнала
	double	signals_sum_of_sqr{0.0};					
	for (int i = 0;	i < N; ++i)
		signals_sum_of_sqr += std::pow((signals_array[i] - mean_value), 2);
	
	//	Возвращаем дисперсию отсчетов сигнала
	if (1 == N)
		return 0.0;
	else
		return	signals_sum_of_sqr / (N - 1);		//	Дисперсия случайных величин отсчетов массива сигнала
	/*
		Обращаем внимание, что 'среднее' получается делением на (N-1) вместо N. 
	*/
}
//	'Стандартное отклонение' случайных величин массива как корень квадратный из дисперсии
static double 	standard_deviation	(	double 	variance		//	Регрессия
									)	{
	//cout << "-------------- I'm " <<  __PRETTY_FUNCTION__  << endl;	
	return 	std::sqrt(variance);
}
